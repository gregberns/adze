func TestRenderBasicStructure(t *testing.T) {
	cfg := &config.Config{
		Name:     "test",
		Platform: "darwin",
		Secrets:  []config.SecretEntry{},
	}

	graph := &dag.ResolvedGraph{
		Steps: []dag.ResolvedStep{
			{Name: "test-step", DependsOn: map[string]string{}, Depth: 0},
		},
	}

	stepConfigs := []step.StepConfig{
		{
			Name:  "test-step",
			Check: &step.ShellCommand{Args: []string{"sh", "-c", "command -v git"}},
			Apply: &step.ShellCommand{Args: []string{"sh", "-c", "brew install git"}},
		},
	}

	script, err := Render(cfg, graph, stepConfigs, "darwin", "test.yaml")
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	// Check shebang
	if !strings.HasPrefix(script, "#!/bin/bash\n") {
		t.Error("script should start with #!/bin/bash")
	}

	// Check header comment
	if !strings.Contains(script, "Generated by adze from test.yaml") {
		t.Error("script should contain config path in header")
	}
	if !strings.Contains(script, "Platform: darwin") {
		t.Error("script should contain platform in header")
	}
	if !strings.Contains(script, "Steps: 1") {
		t.Error("script should contain step count in header")
	}

	// Check set -euo pipefail
	if !strings.Contains(script, "set -euo pipefail") {
		t.Error("script should contain set -euo pipefail")
	}

	// Check counter variables
	if !strings.Contains(script, "SUCCEEDED=0") {
		t.Error("script should initialize SUCCEEDED counter")
	}
	if !strings.Contains(script, "SKIPPED=0") {
		t.Error("script should initialize SKIPPED counter")
	}
	if !strings.Contains(script, "FAILED=0") {
		t.Error("script should initialize FAILED counter")
	}

	// Check summary footer
	if !strings.Contains(script, "=== Summary ===") {
		t.Error("script should contain summary footer")
	}
}

