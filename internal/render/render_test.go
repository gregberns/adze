func TestRenderMultipleStepsOrder(t *testing.T) {
	cfg := &config.Config{
		Name:     "test",
		Platform: "darwin",
		Secrets:  []config.SecretEntry{},
	}

	graph := &dag.ResolvedGraph{
		Steps: []dag.ResolvedStep{
			{Name: "first", DependsOn: map[string]string{}, Depth: 0},
			{Name: "second", DependsOn: map[string]string{"first-cap": "first"}, Depth: 1},
			{Name: "third", DependsOn: map[string]string{"second-cap": "second"}, Depth: 2},
		},
	}

	stepConfigs := []step.StepConfig{
		{
			Name:  "first",
			Check: &step.ShellCommand{Args: []string{"sh", "-c", "check-first"}},
			Apply: &step.ShellCommand{Args: []string{"sh", "-c", "apply-first"}},
		},
		{
			Name:  "second",
			Check: &step.ShellCommand{Args: []string{"sh", "-c", "check-second"}},
			Apply: &step.ShellCommand{Args: []string{"sh", "-c", "apply-second"}},
		},
		{
			Name:  "third",
			Check: &step.ShellCommand{Args: []string{"sh", "-c", "check-third"}},
			Apply: &step.ShellCommand{Args: []string{"sh", "-c", "apply-third"}},
		},
	}

	script, err := Render(cfg, graph, stepConfigs, "darwin", "test.yaml")
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	// Steps should appear in DAG order
	firstIdx := strings.Index(script, "[1/3] first")
	secondIdx := strings.Index(script, "[2/3] second")
	thirdIdx := strings.Index(script, "[3/3] third")

	if firstIdx == -1 || secondIdx == -1 || thirdIdx == -1 {
		t.Fatalf("expected all step headers in output, got:\n%s", script)
	}

	if firstIdx >= secondIdx || secondIdx >= thirdIdx {
		t.Error("steps should appear in DAG order: first, second, third")
	}
}

