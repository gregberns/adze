func TestRenderBatchStep(t *testing.T) {
	cfg := &config.Config{
		Name:     "test",
		Platform: "darwin",
		Secrets:  []config.SecretEntry{},
	}

	graph := &dag.ResolvedGraph{
		Steps: []dag.ResolvedStep{
			{Name: "brew-packages", DependsOn: map[string]string{}, Depth: 0},
		},
	}

	stepConfigs := []step.StepConfig{
		{
			Name: "brew-packages",
			Items: []step.StepItem{
				{Name: "git"},
				{Name: "curl"},
				{Name: "jq"},
			},
		},
	}

	script, err := Render(cfg, graph, stepConfigs, "darwin", "test.yaml")
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	// Should have batch header
	if !strings.Contains(script, "[1/1] brew-packages (3 items)") {
		t.Error("script should contain batch step header with item count")
	}

	// Should have per-item blocks
	if !strings.Contains(script, "# git") {
		t.Error("script should contain git item comment")
	}
	if !strings.Contains(script, "# curl") {
		t.Error("script should contain curl item comment")
	}
	if !strings.Contains(script, "# jq") {
		t.Error("script should contain jq item comment")
	}

	// Should have per-item check (brew list)
	if !strings.Contains(script, "brew list --formula git") {
		t.Error("script should contain brew check for git")
	}

	// Should have per-item apply (brew install)
	if !strings.Contains(script, "brew install git") {
		t.Error("script should contain brew install for git")
	}

	// Should have batch summary
	if !strings.Contains(script, "brew-packages: $step_ok applied, $step_skip present, $step_fail failed") {
		t.Error("script should contain batch step summary")
	}
}

