func TestShellCommandToString(t *testing.T) {
	tests := []struct {
		name string
		cmd  *step.ShellCommand
		want string
	}{
		{
			name: "nil command",
			cmd:  nil,
			want: "",
		},
		{
			name: "empty args",
			cmd:  &step.ShellCommand{Args: []string{}},
			want: "",
		},
		{
			name: "sh -c pattern",
			cmd:  &step.ShellCommand{Args: []string{"sh", "-c", "echo hello"}},
			want: "echo hello",
		},
		{
			name: "direct command",
			cmd:  &step.ShellCommand{Args: []string{"brew", "install", "git"}},
			want: "brew install git",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := shellCommandToString(tt.cmd)
			if got != tt.want {
				t.Errorf("shellCommandToString() = %q, want %q", got, tt.want)
			}
		})
	}
}

